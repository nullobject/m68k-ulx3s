void oled_clear() {
  cmd_set_column_address(0x1C, 0x5B);
  cmd_set_row_address(0x00, 0x3F);
  cmd_write_ram();

  for (uint8_t i = 0; i < 64; i++) {
    for (uint8_t j = 0; j < 128; j++) {
      data(0x00);
    }
  }
}

void oled_fill() {
  cmd_set_column_address(0x1C, 0x5B);
  cmd_set_row_address(0x00, 0x3F);
  cmd_write_ram();

  for (uint8_t i = 0; i < 32; i++) {
    for (uint8_t j = 0; j < 64; j++) {
      data(0xFF);
      data(0x00);
    }
    for (uint8_t j = 0; j < 64; j++) {
      data(0x00);
      data(0xFF);
    }
  }
}

void oled_image(uint8_t *image) {
  cmd_set_remap_format(0x14, 0x11);
  cmd_set_column_address(0x1C, 0x5B);
  cmd_set_row_address(0x00, 0x3F);
  cmd_write_ram();

  for (uint8_t i = 0; i < 64; i++) {
    for (uint8_t j = 0; j < 32; j++) {
      uint8_t buff = *image;
      buff = ((buff >> 6) & 0x03);
      if (buff == 0x03) {
        data(0xFF);
      } else if (buff == 0x02) {
        data(0xF0);
      } else if (buff == 0x01) {
        data(0x0F);
      } else {
        data(0x00);
      }

      buff = *image;
      buff = ((buff >> 4) & 0x03);
      if (buff == 0x03) {
        data(0xFF);
      } else if (buff == 0x02) {
        data(0xF0);
      } else if (buff == 0x01) {
        data(0x0F);
      } else {
        data(0x00);
      }

      buff = *image;
      buff = ((buff >> 2) & 0x03);
      if (buff == 0x03) {
        data(0xFF);
      } else if (buff == 0x02) {
        data(0xF0);
      } else if (buff == 0x01) {
        data(0x0F);
      } else {
        data(0x00);
      }

      buff = *image;
      buff = (buff & 0x03);
      if (buff == 0x03) {
        data(0xFF);
      } else if (buff == 0x02) {
        data(0xF0);
      } else if (buff == 0x01) {
        data(0x0F);
      } else {
        data(0x00);
      }

      image++;
    }
  }
}

void oled_init() {
  cmd_unlock(0x12);
  cmd_set_display_enable(false);
  cmd_set_display_clock(0x91);
  cmd_set_mux_ratio(0x3F);
  cmd_set_display_offset(0x00);
  cmd_set_start_line(0x00);
  cmd_set_remap_format(0x14, 0x11);
  cmd_set_gpio(0x00);
  cmd_set_function_selection(0x01);
  cmd_set_display_enhancement_a(0xA0, 0xFD); // enable external VSL
  cmd_set_contract_current(0xCF);
  cmd_set_master_current(0x0F);
  cmd_set_linear_grayscale_table();
  cmd_set_phase_length(0xE2);
  cmd_set_display_enhancement_b(0xA2);
  cmd_set_precharge_voltage(0X1F);
  cmd_set_precharge_period(0x08);
  cmd_set_vcomh(0x07);
  cmd_set_display_mode(OLED_DISPLAY_MODE_NORMAL);
  cmd_set_display_enable(true);
}

void start(void) {
  digital_write(OLED_CS, LOW);
  digital_write(OLED_RW, LOW);
  digital_write(OLED_RES, LOW);
  delay(100);
  digital_write(OLED_RES, HIGH);
  delay(100);

  oled_init();
  delay(100);

  while (1) {
    *LED = 1;
    oled_clear();
    delay(100);
    oled_fill();
    delay(100);

    *LED = 0;
    oled_clear();
    delay(100);
    oled_image(logo);
    delay(100);
  }
}

int __attribute__((noreturn)) main(void) {
  asm("dc.l 0x2000"); // Set stack to top of RAM
  asm("dc.l start");
  __builtin_unreachable();
}
